<?php
    function penjumlahan($angka1,$angka2){
        return $angka1 + $angka2;
    }

    function pengurangan($angka1,$angka2){
        return $angka1 - $angka2;
    }

    function perkalian($angka1,$angka2){
        return $angka1 * $angka2;
    }

    function pembagian($angka1,$angka2){
        return $angka1 / $angka2;
    }

    echo penjumlahan(1,2);